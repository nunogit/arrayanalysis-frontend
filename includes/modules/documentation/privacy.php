

<h1>Privacy</h1>

Array analysis allows you to do the reports in a complete anonymous way. Optionaly you are given the oportunity to insert an email. You can later retrieve your data or reports either by using an handler or the link sent by mail.


<h2>The handler</h2>
<p>The handler is a text string that identifies your data.</p>
<p>An handler is useful if you do not wish to provide an email and/or wish to do analysis without waiting for the results. The handler allows you to retrieve information later.</p>
<p>Besides the handler/data association no other information is kept about the data sender.</p>

<h4>The handler is useful to:</h4>

<ul>
	<li> Avoid uploading raw data again</li>
	<li> Do calculations and retrieve the results later </li>
	<li> Reuse previous results (clean files) in subsequent calculations</li>
</ul>

<h4>Keep the handler / email link safe</h4>
Remember that the handler or email link sent to you identify your data. Keep it safe.

<h2>How long is my data kept?</h2>

<p>Your data gets delete 2 weeks after your last interaction with the system.</p>
<p>If you wish to delete it sooner you can do it by: using the handler or the email link sent to you</p>

